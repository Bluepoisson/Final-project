import React from 'react';

const CheckboxTime = ({ waitingTime, onWaitingTimeChange }) => {


  return (
    <div className="question-card">
      <h3 className="question-header" tabIndex="0">Was your appointment-time respected?</h3>
      <label htmlFor="yes">Yes</label>
      <input
        id="yes"
        type="checkbox"
        checked={waitingTime.includes('yes')}
        onChange={() => onWaitingTimeChange('yes')}
      />
      <label htmlFor="somewhat">Sometimes</label>
      <input
        id="somewhat"
        type="checkbox"
        checked={waitingTime.includes('somewhat')}
        onChange={() => onWaitingTimeChange('somewhat')}
      />
      <label htmlFor="no">Often</label>
      <input
        id="no"
        type="checkbox"
        checked={waitingTime.includes('no')}
        onChange={() => onWaitingTimeChange('no')}
      />
    </div>
  );

};

export default CheckboxTime;